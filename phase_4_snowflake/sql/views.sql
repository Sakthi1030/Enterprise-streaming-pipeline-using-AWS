-- Product Performance View
CREATE OR REPLACE VIEW ANALYTICS.VW_PRODUCT_PERFORMANCE AS
SELECT
    p.PRODUCT_ID,
    p.PRODUCT_NAME,
    p.CATEGORY,
    p.SUBCATEGORY,
    p.BRAND,
    p.PRODUCT_LIFECYCLE_STAGE,
    p.PROFITABILITY_SEGMENT,
    p.STOCK_STATUS,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TIMES_SOLD,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_SOLD,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_REVENUE,
