-- Customer Lifetime Value View
CREATE OR REPLACE VIEW ANALYTICS.VW_CUSTOMER_LIFETIME_VALUE AS
SELECT
    c.CUSTOMER_ID,
    c.CUSTOMER_NAME,
    c.CUSTOMER_SEGMENT,
    c.CUSTOMER_TIER,
    c.RFM_SEGMENT,
    c.REGION,
    c.AGE_GROUP,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_PURCHASED,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_SPENT,
    AVG(fs.LINE_FINAL_AMOUNT) AS AVG_TRANSACTION_VALUE,
    MIN(fs.TRANSACTION_TIMESTAMP) AS FIRST_PURCHASE_DATE,
    MAX(fs.TRANSACTION_TIMESTAMP) AS LAST_PURCHASE_DATE,
    DATEDIFF('day', MIN(fs.TRANSACTION_TIMESTAMP), MAX(fs.TRANSACTION_TIMESTAMP)) AS CUSTOMER_LIFETIME_DAYS,
    CASE 
        WHEN DATEDIFF('day', MIN(fs.TRANSACTION_TIMESTAMP), MAX(fs.TRANSACTION_TIMESTAMP)) > 0 
        THEN SUM(fs.LINE_FINAL_AMOUNT) / DATEDIFF('day', MIN(fs.TRANSACTION_TIMESTAMP), MAX(fs.TRANSACTION_TIMESTAMP)) 
        ELSE SUM(fs.LINE_FINAL_AMOUNT) 
    END AS DAILY_SPENDING_RATE,
    CASE 
        WHEN COUNT(DISTINCT fs.TRANSACTION_ID) > 1 
        THEN DATEDIFF('day', MIN(fs.TRANSACTION_TIMESTAMP), MAX(fs.TRANSACTION_TIMESTAMP)) / (COUNT(DISTINCT fs.TRANSACTION_ID) - 1) 
        ELSE NULL 
    END AS AVG_PURCHASE_FREQUENCY_DAYS
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_CUSTOMER c ON fs.CUSTOMER_SK = c.CUSTOMER_SK AND c.IS_CURRENT = TRUE
GROUP BY c.CUSTOMER_ID, c.CUSTOMER_NAME, c.CUSTOMER_SEGMENT, c.CUSTOMER_TIER, c.RFM_SEGMENT, c.REGION, c.AGE_GROUP
ORDER BY TOTAL_SPENT DESC;

-- Customer Segmentation Analysis View
CREATE OR REPLACE VIEW ANALYTICS.VW_CUSTOMER_SEGMENT_ANALYSIS AS
SELECT
    c.CUSTOMER_SEGMENT,
    c.CUSTOMER_TIER,
    c.RFM_SEGMENT,
    COUNT(DISTINCT c.CUSTOMER_ID) AS CUSTOMER_COUNT,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_REVENUE,
    AVG(fs.LINE_FINAL_AMOUNT) AS AVG_REVENUE_PER_CUSTOMER,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    AVG(fs.QUANTITY) AS AVG_ITEMS_PER_TRANSACTION,
    SUM(CASE WHEN fs.IS_FIRST_PURCHASE THEN 1 ELSE 0 END) AS NEW_CUSTOMERS,
    SUM(CASE WHEN fs.IS_REPEAT_PURCHASE THEN 1 ELSE 0 END) AS REPEAT_CUSTOMERS,
    AVG(fs.LINE_PROFIT_MARGIN) AS AVG_PROFIT_MARGIN,
    SUM(fs.LINE_DISCOUNT_AMOUNT) AS TOTAL_DISCOUNT_GIVEN
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_CUSTOMER c ON fs.CUSTOMER_SK = c.CUSTOMER_SK AND c.IS_CURRENT = TRUE
GROUP BY c.CUSTOMER_SEGMENT, c.CUSTOMER_TIER, c.RFM_SEGMENT
ORDER BY TOTAL_REVENUE DESC;

-- Customer Churn Risk View
CREATE OR REPLACE VIEW ANALYTICS.VW_CUSTOMER_CHURN_RISK AS
SELECT
    c.CUSTOMER_ID,
    c.CUSTOMER_NAME,
    c.RECENCY_SEGMENT,
    c.FREQUENCY_SEGMENT,
    c.VALUE_SEGMENT,
    c.RFM_SEGMENT,
    DATEDIFF('day', MAX(fs.TRANSACTION_TIMESTAMP), CURRENT_DATE()) AS DAYS_SINCE_LAST_PURCHASE,
    CASE
        WHEN DATEDIFF('day', MAX(fs.TRANSACTION_TIMESTAMP), CURRENT_DATE()) > 180 THEN 'HIGH'
        WHEN DATEDIFF('day', MAX(fs.TRANSACTION_TIMESTAMP), CURRENT_DATE()) > 90 THEN 'MEDIUM'
        WHEN DATEDIFF('day', MAX(fs.TRANSACTION_TIMESTAMP), CURRENT_DATE()) > 30 THEN 'LOW'
        ELSE 'ACTIVE'
    END AS CHURN_RISK_LEVEL,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_SPENT,
    MAX(fs.TRANSACTION_TIMESTAMP) AS LAST_PURCHASE_DATE
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_CUSTOMER c ON fs.CUSTOMER_SK = c.CUSTOMER_SK AND c.IS_CURRENT = TRUE
GROUP BY c.CUSTOMER_ID, c.CUSTOMER_NAME, c.RECENCY_SEGMENT, c.FREQUENCY_SEGMENT, c.VALUE_SEGMENT, c.RFM_SEGMENT
HAVING DATEDIFF('day', MAX(fs.TRANSACTION_TIMESTAMP), CURRENT_DATE()) > 30
ORDER BY DAYS_SINCE_LAST_PURCHASE DESC, TOTAL_SPENT DESC;

