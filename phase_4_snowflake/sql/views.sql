-- Profitability Analysis View
CREATE OR REPLACE VIEW ANALYTICS.VW_PROFITABILITY_ANALYSIS AS
SELECT
    d.YEAR,
    d.MONTH,
    d.MONTH_NAME,
    p.CATEGORY,
    p.SUBCATEGORY,
    p.BRAND,
    c.CUSTOMER_SEGMENT,
    c.CUSTOMER_TIER,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_SOLD,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_REVENUE,
    SUM(fs.LINE_COST_AMOUNT) AS TOTAL_COST,
    SUM(fs.LINE_PROFIT_AMOUNT) AS TOTAL_PROFIT,
    AVG(fs.LINE_PROFIT_MARGIN) AS AVG_PROFIT_MARGIN,
    SUM(fs.LINE_DISCOUNT_AMOUNT) AS TOTAL_DISCOUNT,
    (SUM(fs.LINE_PROFIT_AMOUNT) / SUM(fs.LINE_FINAL_AMOUNT)) * 100 AS OVERALL_PROFIT_MARGIN_PERCENT
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_DATE d ON fs.DATE_KEY = d.DATE_KEY
JOIN ANALYTICS.DIM_PRODUCT p ON fs.PRODUCT_SK = p.PRODUCT_SK AND p.IS_CURRENT = TRUE
JOIN ANALYTICS.DIM_CUSTOMER c ON fs.CUSTOMER_SK = c.CUSTOMER_SK AND c.IS_CURRENT = TRUE
GROUP BY d.YEAR, d.MONTH, d.MONTH_NAME, p.CATEGORY, p.SUBCATEGORY, p.BRAND, c.CUSTOMER_SEGMENT, c.CUSTOMER_TIER
ORDER BY d.YEAR DESC, d.MONTH DESC, TOTAL_PROFIT DESC;

-- ============================================
-- OPERATIONAL ANALYTICS VIEWS
-- ============================================

-- Fraud Detection Analysis View
CREATE OR REPLACE VIEW ANALYTICS.VW_FRAUD_DETECTION_ANALYSIS AS
SELECT
    DATE(fs.TRANSACTION_TIMESTAMP) AS TRANSACTION_DATE,
    pay.PAYMENT_METHOD,
    ch.CHANNEL_TYPE,
    COUNT(*) AS TOTAL_TRANSACTIONS,
    SUM(CASE WHEN fs.FRAUD_DETECTED THEN 1 ELSE 0 END) AS FRAUDULENT_TRANSACTIONS,
    SUM(CASE WHEN fs.FRAUD_DETECTED THEN fs.LINE_FINAL_AMOUNT ELSE 0 END) AS FRAUD_AMOUNT,
    CASE 
        WHEN COUNT(*) > 0 
        THEN SUM(CASE WHEN fs.FRAUD_DETECTED THEN 1 ELSE 0 END) * 100.0 / COUNT(*) 
        ELSE 0 
    END AS FRAUD_RATE_PERCENT,
    AVG(CASE WHEN fs.FRAUD_DETECTED THEN fs.FRAUD_SCORE ELSE NULL END) AS AVG_FRAUD_SCORE
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_PAYMENT pay ON fs.PAYMENT_SK = pay.PAYMENT_SK
JOIN ANALYTICS.DIM_CHANNEL ch ON fs.CHANNEL_SK = ch.CHANNEL_SK
GROUP BY DATE(fs.TRANSACTION_TIMESTAMP), pay.PAYMENT_METHOD, ch.CHANNEL_TYPE
ORDER BY TRANSACTION_DATE DESC, FRAUD_RATE_PERCENT DESC;

