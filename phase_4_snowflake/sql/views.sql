-- Executive Dashboard Summary View
CREATE OR REPLACE VIEW DATA_MART.VW_EXECUTIVE_DASHBOARD AS
SELECT
    'SALES' AS METRIC_CATEGORY,
    'Total Revenue' AS METRIC_NAME,
    SUM(LINE_FINAL_AMOUNT) AS METRIC_VALUE,
    'CURRENCY' AS METRIC_UNIT
