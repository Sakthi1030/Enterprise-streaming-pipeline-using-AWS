-- Daily Sales Summary View
CREATE OR REPLACE VIEW ANALYTICS.VW_DAILY_SALES_SUMMARY AS
SELECT
    d.DATE,
    d.YEAR,
    d.MONTH,
    d.MONTH_NAME,
    d.DAY_NAME,
    d.IS_WEEKEND,
    d.SEASON,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    COUNT(DISTINCT fs.CUSTOMER_SK) AS UNIQUE_CUSTOMERS,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_SOLD,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_SALES,
    SUM(fs.LINE_COST_AMOUNT) AS TOTAL_COST,
    SUM(fs.LINE_PROFIT_AMOUNT) AS TOTAL_PROFIT,
    AVG(fs.LINE_PROFIT_MARGIN) AS AVG_PROFIT_MARGIN,
    SUM(fs.LINE_DISCOUNT_AMOUNT) AS TOTAL_DISCOUNT,
    COUNT(DISTINCT fs.PRODUCT_SK) AS UNIQUE_PRODUCTS_SOLD,
    SUM(CASE WHEN fs.FRAUD_DETECTED THEN fs.LINE_FINAL_AMOUNT ELSE 0 END) AS FRAUD_AMOUNT,
    SUM(CASE WHEN fs.FRAUD_DETECTED THEN 1 ELSE 0 END) AS FRAUD_TRANSACTIONS
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_DATE d ON fs.DATE_KEY = d.DATE_KEY
GROUP BY d.DATE, d.YEAR, d.MONTH, d.MONTH_NAME, d.DAY_NAME, d.IS_WEEKEND, d.SEASON
ORDER BY d.DATE DESC;

-- Monthly Sales Trend View
CREATE OR REPLACE VIEW ANALYTICS.VW_MONTHLY_SALES_TREND AS
SELECT
    d.YEAR,
    d.MONTH,
    d.MONTH_NAME,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    COUNT(DISTINCT fs.CUSTOMER_SK) AS UNIQUE_CUSTOMERS,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_SOLD,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_SALES,
    SUM(fs.LINE_PROFIT_AMOUNT) AS TOTAL_PROFIT,
    AVG(fs.LINE_PROFIT_MARGIN) AS AVG_PROFIT_MARGIN,
    LAG(SUM(fs.LINE_FINAL_AMOUNT), 1) OVER (ORDER BY d.YEAR, d.MONTH) AS PREV_MONTH_SALES,
    SUM(fs.LINE_FINAL_AMOUNT) - LAG(SUM(fs.LINE_FINAL_AMOUNT), 1) OVER (ORDER BY d.YEAR, d.MONTH) AS SALES_GROWTH,
    CASE 
        WHEN LAG(SUM(fs.LINE_FINAL_AMOUNT), 1) OVER (ORDER BY d.YEAR, d.MONTH) > 0 
        THEN ((SUM(fs.LINE_FINAL_AMOUNT) - LAG(SUM(fs.LINE_FINAL_AMOUNT), 1) OVER (ORDER BY d.YEAR, d.MONTH)) / 
              LAG(SUM(fs.LINE_FINAL_AMOUNT), 1) OVER (ORDER BY d.YEAR, d.MONTH)) * 100 
        ELSE NULL 
    END AS SALES_GROWTH_PERCENT
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_DATE d ON fs.DATE_KEY = d.DATE_KEY
GROUP BY d.YEAR, d.MONTH, d.MONTH_NAME
ORDER BY d.YEAR DESC, d.MONTH DESC;

-- Hourly Sales Pattern View
CREATE OR REPLACE VIEW ANALYTICS.VW_HOURLY_SALES_PATTERN AS
SELECT
    t.HOUR,
    t.TIME_PERIOD,
    t.IS_BUSINESS_HOURS,
    t.IS_PEAK_HOURS,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_SALES,
    AVG(fs.LINE_FINAL_AMOUNT) AS AVG_TRANSACTION_VALUE,
    COUNT(DISTINCT fs.CUSTOMER_SK) AS UNIQUE_CUSTOMERS,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_SOLD
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_TIME t ON fs.TIME_KEY = t.TIME_KEY
GROUP BY t.HOUR, t.TIME_PERIOD, t.IS_BUSINESS_HOURS, t.IS_PEAK_HOURS
ORDER BY t.HOUR;

