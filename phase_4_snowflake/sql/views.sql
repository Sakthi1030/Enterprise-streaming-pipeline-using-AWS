-- Product Inventory Health View
CREATE OR REPLACE VIEW ANALYTICS.VW_PRODUCT_INVENTORY_HEALTH AS
SELECT
    p.PRODUCT_ID,
    p.PRODUCT_NAME,
    p.CATEGORY,
    p.SUBCATEGORY,
    p.STOCK_QUANTITY,
    p.STOCK_STATUS,
    p.REORDER_LEVEL,
    COALESCE(SUM(fs.QUANTITY), 0) AS LAST_30_DAY_SALES,
    CASE 
        WHEN SUM(fs.QUANTITY) > 0 
        THEN p.STOCK_QUANTITY / (SUM(fs.QUANTITY) / 30.0) 
        ELSE NULL 
    END AS DAYS_OF_SUPPLY,
    CASE
        WHEN p.STOCK_STATUS = 'Out of Stock' THEN 'CRITICAL'
        WHEN p.STOCK_STATUS = 'Low Stock' THEN 'WARNING'
        WHEN DAYS_OF_SUPPLY < 7 THEN 'LOW_SUPPLY'
        WHEN DAYS_OF_SUPPLY > 90 THEN 'OVERSTOCKED'
        ELSE 'HEALTHY'
    END AS INVENTORY_HEALTH_STATUS,
    p.CURRENT_PRICE * p.STOCK_QUANTITY AS INVENTORY_VALUE
FROM ANALYTICS.DIM_PRODUCT p
LEFT JOIN ANALYTICS.FACT_SALES fs ON p.PRODUCT_SK = fs.PRODUCT_SK 
    AND fs.TRANSACTION_TIMESTAMP >= DATEADD('day', -30, CURRENT_DATE())
WHERE p.IS_CURRENT = TRUE
GROUP BY p.PRODUCT_ID, p.PRODUCT_NAME, p.CATEGORY, p.SUBCATEGORY, 
         p.STOCK_QUANTITY, p.STOCK_STATUS, p.REORDER_LEVEL, p.CURRENT_PRICE
ORDER BY INVENTORY_HEALTH_STATUS, DAYS_OF_SUPPLY;

