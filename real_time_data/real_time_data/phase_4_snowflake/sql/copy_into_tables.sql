-- Load Date Dimension (for a 10-year period)
INSERT INTO ANALYTICS.DIM_DATE
SELECT
    TO_NUMBER(TO_CHAR(DATE, 'YYYYMMDD')) AS DATE_KEY,
    DATE,
    YEAR(DATE) AS YEAR,
    QUARTER(DATE) AS QUARTER,
    MONTH(DATE) AS MONTH,
    MONTHNAME(DATE) AS MONTH_NAME,
    DAY(DATE) AS DAY_OF_MONTH,
    DAYOFWEEK(DATE) AS DAY_OF_WEEK,
    DAYNAME(DATE) AS DAY_NAME,
    WEEKOFYEAR(DATE) AS WEEK_OF_YEAR,
    DAYOFWEEK(DATE) IN (6, 7) AS IS_WEEKEND,
    FALSE AS IS_HOLIDAY, -- Would be populated from holiday calendar
    NULL AS HOLIDAY_NAME,
    YEAR(DATE) AS FISCAL_YEAR, -- Assuming calendar year fiscal
    QUARTER(DATE) AS FISCAL_QUARTER,
    MONTH(DATE) AS FISCAL_MONTH,
    CASE 
        WHEN MONTH(DATE) IN (12, 1, 2) THEN 'Winter'
        WHEN MONTH(DATE) IN (3, 4, 5) THEN 'Spring'
        WHEN MONTH(DATE) IN (6, 7, 8) THEN 'Summer'
        ELSE 'Fall'
    END AS SEASON
FROM (
    SELECT DATEADD(DAY, SEQ4(), '2020-01-01') AS DATE
    FROM TABLE(GENERATOR(ROWCOUNT => 3650)) -- 10 years
);

-- Load Time Dimension
INSERT INTO ANALYTICS.DIM_TIME
SELECT
    (HOUR * 100) + MINUTE AS TIME_KEY,
    HOUR,
    MINUTE,
    HOUR AS HOUR_OF_DAY,
    CASE
        WHEN HOUR BETWEEN 5 AND 11 THEN 'Morning'
        WHEN HOUR BETWEEN 12 AND 16 THEN 'Afternoon'
        WHEN HOUR BETWEEN 17 AND 21 THEN 'Evening'
        ELSE 'Night'
    END AS TIME_PERIOD,
    HOUR BETWEEN 9 AND 17 AS IS_BUSINESS_HOURS,
    HOUR IN (12, 13, 18, 19) AS IS_PEAK_HOURS,
    CURRENT_TIMESTAMP()
FROM (
    SELECT HOUR, MINUTE
    FROM (
        SELECT SEQ4() AS HOUR FROM TABLE(GENERATOR(ROWCOUNT => 24))
    ) HOURS
    CROSS JOIN (
        SELECT SEQ4() AS MINUTE FROM TABLE(GENERATOR(ROWCOUNT => 60))
    ) MINUTES
);

