    st.SUBTOTAL_AMOUNT,
    st.DISCOUNT_AMOUNT,
    st.TAX_AMOUNT,
    st.SHIPPING_AMOUNT,
    st.TOTAL_AMOUNT,
    st.PAYMENT_ID,
    st.PAYMENT_STATUS,
    st.FRAUD_DETECTED,
    st.FRAUD_SCORE,
    CASE
        WHEN st.FRAUD_SCORE > 70 THEN 'High'
        WHEN st.FRAUD_SCORE > 30 THEN 'Medium'
        ELSE 'Low'
    END AS FRAUD_RISK_LEVEL,
    st.FULFILLMENT_STATUS,
    st.SHIPPING_METHOD,
    st.TRACKING_NUMBER,
    st.ESTIMATED_DELIVERY_DATE,
    NULL AS ACTUAL_DELIVERY_DATE, -- Would come from fulfillment system
    NULL AS DELIVERY_DAYS,
    ROW_NUMBER() OVER (PARTITION BY st.CUSTOMER_ID ORDER BY st.TRANSACTION_TIMESTAMP) = 1 AS IS_FIRST_PURCHASE,
    ROW_NUMBER() OVER (PARTITION BY st.CUSTOMER_ID ORDER BY st.TRANSACTION_TIMESTAMP) > 1 AS IS_REPEAT_PURCHASE,
    FALSE AS IS_CROSS_SELL, -- Would need business logic
    FALSE AS IS_UP_SELL, -- Would need business logic
    ti.QUANTITY > 5 AS IS_BULK_PURCHASE,
    ti.DISCOUNT_PERCENTAGE > 0 AS IS_PROMOTIONAL_ITEM,
    NULL AS SESSION_ID, -- From web analytics
    st.DEVICE_TYPE || ' ' || st.BROWSER AS USER_AGENT,
    st.IP_ADDRESS,
    NULL AS REFERRER_URL, -- From web analytics
    NULL AS LANDING_PAGE_URL, -- From web analytics
    'BATCH_' || CURRENT_DATE()::STRING,
    'SALES_PLATFORM'
FROM STAGING.TRANSACTIONS st
JOIN STAGING.TRANSACTION_ITEMS ti ON st.TRANSACTION_ID = ti.TRANSACTION_ID
JOIN ANALYTICS.DIM_DATE d ON DATE(st.TRANSACTION_TIMESTAMP) = d.DATE
JOIN ANALYTICS.DIM_TIME t ON EXTRACT(HOUR FROM st.TRANSACTION_TIMESTAMP) = t.HOUR
    AND EXTRACT(MINUTE FROM st.TRANSACTION_TIMESTAMP) = t.MINUTE
JOIN ANALYTICS.DIM_CUSTOMER c ON st.CUSTOMER_ID = c.CUSTOMER_ID AND c.IS_CURRENT = TRUE
JOIN ANALYTICS.DIM_PRODUCT p ON ti.PRODUCT_ID = p.PRODUCT_ID AND p.IS_CURRENT = TRUE
LEFT JOIN ANALYTICS.DIM_STORE s ON st.STORE_ID = s.STORE_ID AND s.IS_CURRENT = TRUE
LEFT JOIN ANALYTICS.DIM_CAMPAIGN cam ON st.CAMPAIGN_ID = cam.CAMPAIGN_ID AND cam.IS_CURRENT = TRUE
JOIN ANALYTICS.DIM_PAYMENT pay ON st.PAYMENT_METHOD = pay.PAYMENT_METHOD
JOIN ANALYTICS.DIM_CHANNEL ch ON st.CHANNEL = ch.CHANNEL_NAME
    AND st.DEVICE_TYPE = ch.DEVICE_CATEGORY
WHERE st.IS_VALID = TRUE
AND st.TRANSACTION_TIMESTAMP IS NOT NULL;

