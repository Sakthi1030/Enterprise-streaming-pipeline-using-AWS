INSERT INTO ANALYTICS.FACT_DAILY_SALES_SNAPSHOT (
    SNAPSHOT_DATE,
    DATE_KEY,
    CUSTOMER_SK,
    PRODUCT_SK,
    STORE_SK,
    TOTAL_TRANSACTIONS,
    TOTAL_LINE_ITEMS,
    TOTAL_QUANTITY_SOLD,
    TOTAL_CUSTOMERS,
    TOTAL_SALES_AMOUNT,
    TOTAL_DISCOUNT_AMOUNT,
    TOTAL_TAX_AMOUNT,
    TOTAL_SHIPPING_AMOUNT,
    TOTAL_NET_SALES,
    TOTAL_COST_AMOUNT,
    TOTAL_PROFIT_AMOUNT,
    AVERAGE_PROFIT_MARGIN,
    AVERAGE_TRANSACTION_VALUE,
    AVERAGE_ITEMS_PER_TRANSACTION,
    AVERAGE_ITEM_VALUE,
    NEW_CUSTOMERS_COUNT,
    REPEAT_CUSTOMERS_COUNT,
    CHURNED_CUSTOMERS_COUNT,
    TOP_SELLING_PRODUCT_ID,
    TOP_SELLING_CATEGORY,
    FRAUDULENT_TRANSACTIONS_COUNT,
    FRAUD_AMOUNT,
    FRAUD_RATE_PERCENT,
    IS_COMPLETE
)
SELECT
    DATE(fs.TRANSACTION_TIMESTAMP) AS SNAPSHOT_DATE,
    fs.DATE_KEY,
    fs.CUSTOMER_SK,
    fs.PRODUCT_SK,
    fs.STORE_SK,
    COUNT(DISTINCT fs.TRANSACTION_ID) AS TOTAL_TRANSACTIONS,
    COUNT(*) AS TOTAL_LINE_ITEMS,
    SUM(fs.QUANTITY) AS TOTAL_QUANTITY_SOLD,
    COUNT(DISTINCT fs.CUSTOMER_SK) AS TOTAL_CUSTOMERS,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_SALES_AMOUNT,
    SUM(fs.LINE_DISCOUNT_AMOUNT) AS TOTAL_DISCOUNT_AMOUNT,
    SUM(fs.LINE_TAX_AMOUNT) AS TOTAL_TAX_AMOUNT,
    SUM(fs.LINE_SHIPPING_AMOUNT) AS TOTAL_SHIPPING_AMOUNT,
    SUM(fs.LINE_FINAL_AMOUNT) AS TOTAL_NET_SALES,
    SUM(fs.LINE_COST_AMOUNT) AS TOTAL_COST_AMOUNT,
    SUM(fs.LINE_PROFIT_AMOUNT) AS TOTAL_PROFIT_AMOUNT,
    AVG(fs.LINE_PROFIT_MARGIN) AS AVERAGE_PROFIT_MARGIN,
    AVG(fs.TRANSACTION_TOTAL) AS AVERAGE_TRANSACTION_VALUE,
    AVG(fs.QUANTITY) AS AVERAGE_ITEMS_PER_TRANSACTION,
    AVG(fs.UNIT_PRICE) AS AVERAGE_ITEM_VALUE,
    SUM(CASE WHEN fs.IS_FIRST_PURCHASE THEN 1 ELSE 0 END) AS NEW_CUSTOMERS_COUNT,
    SUM(CASE WHEN fs.IS_REPEAT_PURCHASE THEN 1 ELSE 0 END) AS REPEAT_CUSTOMERS_COUNT,
    0 AS CHURNED_CUSTOMERS_COUNT, -- Would need churn logic
    FIRST_VALUE(dp.PRODUCT_ID) OVER (PARTITION BY DATE(fs.TRANSACTION_TIMESTAMP), fs.PRODUCT_SK ORDER BY SUM(fs.QUANTITY) DESC) AS TOP_SELLING_PRODUCT_ID,
    FIRST_VALUE(dp.CATEGORY) OVER (PARTITION BY DATE(fs.TRANSACTION_TIMESTAMP), fs.PRODUCT_SK ORDER BY SUM(fs.QUANTITY) DESC) AS TOP_SELLING_CATEGORY,
    SUM(CASE WHEN fs.FRAUD_DETECTED THEN 1 ELSE 0 END) AS FRAUDULENT_TRANSACTIONS_COUNT,
    SUM(CASE WHEN fs.FRAUD_DETECTED THEN fs.LINE_FINAL_AMOUNT ELSE 0 END) AS FRAUD_AMOUNT,
    CASE 
        WHEN COUNT(*) > 0 THEN SUM(CASE WHEN fs.FRAUD_DETECTED THEN 1 ELSE 0 END) * 100.0 / COUNT(*) 
        ELSE 0 
    END AS FRAUD_RATE_PERCENT,
    TRUE AS IS_COMPLETE
FROM ANALYTICS.FACT_SALES fs
JOIN ANALYTICS.DIM_PRODUCT dp ON fs.PRODUCT_SK = dp.PRODUCT_SK
GROUP BY 
    DATE(fs.TRANSACTION_TIMESTAMP),
    fs.DATE_KEY,
    fs.CUSTOMER_SK,
    fs.PRODUCT_SK,
    fs.STORE_SK,
    dp.PRODUCT_ID,
    dp.CATEGORY;

