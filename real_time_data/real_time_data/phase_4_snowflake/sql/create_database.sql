-- ============================================
-- CREATE AUDIT LOGGING TABLE
-- ============================================

CREATE OR REPLACE TABLE AUDIT.ETL_LOGS (
    LOG_ID NUMBER AUTOINCREMENT,
    PROCESS_NAME VARCHAR(100),
    SCHEMA_NAME VARCHAR(50),
    TABLE_NAME VARCHAR(100),
    RECORDS_PROCESSED NUMBER,
    START_TIME TIMESTAMP_NTZ,
    END_TIME TIMESTAMP_NTZ,
    STATUS VARCHAR(20),
    ERROR_MESSAGE VARCHAR(1000),
    DURATION_SECONDS NUMBER AS (DATEDIFF(SECOND, START_TIME, END_TIME)),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
)
CLUSTER BY (PROCESS_NAME, START_TIME)
COMMENT = 'Audit logs for ETL processes';

-- Create view for monitoring ETL performance
CREATE OR REPLACE VIEW AUDIT.VW_ETL_PERFORMANCE AS
SELECT
    PROCESS_NAME,
    DATE_TRUNC('DAY', START_TIME) AS PROCESS_DATE,
    COUNT(*) AS RUN_COUNT,
    AVG(RECORDS_PROCESSED) AS AVG_RECORDS_PROCESSED,
    AVG(DURATION_SECONDS) AS AVG_DURATION_SECONDS,
    SUM(CASE WHEN STATUS = 'SUCCESS' THEN 1 ELSE 0 END) AS SUCCESS_COUNT,
    SUM(CASE WHEN STATUS = 'FAILED' THEN 1 ELSE 0 END) AS FAILED_COUNT
FROM AUDIT.ETL_LOGS
GROUP BY PROCESS_NAME, DATE_TRUNC('DAY', START_TIME)
ORDER BY PROCESS_DATE DESC;

